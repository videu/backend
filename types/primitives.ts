/** Any string that may be returned from a `typeof` expression. */
export type PrimitiveName =
    'string'
    | 'number'
    | 'bigint'
    | 'boolean'
    | 'symbol'
    | 'undefined'
    | 'object'
    | 'function';

/**
 * The return value of a `typeof` statement for a given primitive type.
 *
 * @param T Any primitive type.
 */
export type PrimitiveNameOf<T> =
    T extends string ? 'string' :
    T extends number ? 'number' :
    T extends boolean ? 'boolean' :
    T extends undefined ? 'undefined' :
    T extends Function ? 'function' : /* tslint:disable-line: ban-types */
    T extends bigint ? 'bigint' :
    T extends symbol ? 'symbol' :
    'object';
